package com.starrypenguin.jpharos.geometry;

/**
 * Point
 * <p/>
 * Points are value classes that represent a location in 3D space
 * <p/>
 * Author: Richard Scott McNew
 */
public class Point {

    public final double x, y, z;

    public Point(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
